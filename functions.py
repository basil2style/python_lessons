#This example is to use function for Adding, Substract, Multiply and Divide

def add(a,b):
    print(f"Adding {a} + {b}")
    return a + b

def substract(a,b):
    print(f"Substract {a} - {b}")
    return a - b

def multiply (a,b):
    print(f"Multiply {a} * {b}")
    return a * b

def divide(a,b):
    print(f"Divide {a} / {b}")
    return a / b

print("Add",add(30,5))
print("Subtract ",substract(30,5))

